#ifndef INTERNAL_VALUE_H
#define INTERNAL_VALUE_H

/* Unicode code points for select characters: */

/* opening parenthesis */
#define UCHAR_OPEN     40 

/* closing parenthesis */
#define UCHAR_CLOSE    41

/* plus sign */
#define UCHAR_PLUS     43

/* hyphen / minus sign */
#define UCHAR_MINUS    45

/* decimal point */
#define UCHAR_DECIMAL  46

/* digit 0 */
#define UCHAR_0        48

/* digit 9 */
#define UCHAR_9        57

/* uppercase Latin E */
#define UCHAR_E        69

/* lowercase Latin e */
#define UCHAR_e       101

#endif

